    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        int filaSeleccionada = tblDatos.getSelectedRow();
        if (filaSeleccionada != -1) {
            String id = tblDatos.getValueAt(filaSeleccionada, 0).toString();
            String nombre = tblDatos.getValueAt(filaSeleccionada, 1).toString();

            String correo = tblDatos.getValueAt(filaSeleccionada, 2).toString();

          
            String telefono = tblDatos.getValueAt(filaSeleccionada, 3).toString();

            // Rellenar los TextField con los valores seleccionados
            txtId.setText(id);
            txtNombres.setText(nombre);
            txtCorreo.setText(correo);
            txtTelefono.setText(telefono);
        }
    }//GEN-LAST:event_tblDatosMouseClicked

